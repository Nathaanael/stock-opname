    /**
     * Return menu groups filtered by current user's role.
     */
    public static function getMenuGroups(): array
    {
        $role = Auth::user()?->role ?? '';

        return [
            [
                'title' => 'Menu',
                'items' => self::getMainNavItems($role),
            ],
        ];
    }

    /**
     * Menu items per role.
     */
    private static function getMainNavItems(string $role): array
    {
        $dashboard = [
            'icon'  => 'dashboard',
            'name'  => 'Dashboard',
            'path'  => '/',
        ];

        $roleMenus = match ($role) {
            'admin_gudang' => [
                [
                    'icon'  => 'dashboard',
                    'name'  => 'Dashboard',
                    'path'  => '/admin-gudang/dashboard',
                ],
                [
                    'icon' => 'tables',
                    'name' => 'Master Barang',
                    'path' => '/admin-gudang/items',
                ],
                [
                    'icon' => 'user-profile',
                    'name' => 'User Profile',
                    'path' => '/profile',
                ],
            ],

            'kasir' => [
                $dashboard,
                // Menu Kasir bisa ditambahkan di sini nanti
            ],

            'owner' => [
                $dashboard,
                // Menu Owner bisa ditambahkan di sini nanti
            ],

            default => [$dashboard],
        };

        return $roleMenus;
    }

